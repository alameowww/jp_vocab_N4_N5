def parse_rows(raw):
    cards = []
    order_by_lesson = {}
    for line in raw.strip().splitlines():
        parts = line.split("|")
        if len(parts) != 10:
            raise ValueError(f"Invalid grammar row ({len(parts)} fields): {line}")
        lesson, slug, title, subtitle, structure, summary, ja, zh, focus, note = parts
        lesson = int(lesson)
        order_by_lesson[lesson] = order_by_lesson.get(lesson, 0) + 1
        cards.append({
            "id": f"lesson-{lesson}-{slug}",
            "lesson": lesson,
            "grammar_id": slug.replace("-", "_"),
            "order": order_by_lesson[lesson],
            "title": title,
            "subtitle": subtitle,
            "structure": structure.split("~"),
            "summary": summary,
            "notes": [note],
            "examples": [{"ja": ja, "zh": zh, "focus": focus}],
            "source_url": SOURCE_URL,
        })
    return cards


def main():
    existing_path = ROOT / "grammar_points.json"
    existing = json.loads(existing_path.read_text(encoding="utf-8"))
    preserved = [card for card in existing if int(card.get("lesson", 0)) in {34, 35}]
    generated = parse_rows(RAW)
    combined = sorted(generated + preserved, key=lambda card: (card["lesson"], card["order"]))
    existing_path.write_text(
        json.dumps(combined, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"Wrote {len(combined)} grammar cards across {len({card['lesson'] for card in combined})} lessons")


if __name__ == "__main__":
    main()
